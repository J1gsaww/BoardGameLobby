export const TYCOON = {
  id: 'tycoon',
  jokers: 2,                 // ค่าตั้งต้น เท่าเกมต้นฉบับ
  revolution: true,
  eightCut: true,
  openingCard: '3C',
  minPlayers: 4,
  maxPlayers: 4
};

/* จำนวนโจ๊กเกอร์ปรับได้ในหน้าตั้งค่าของห้อง */
TYCOON.fromSettings = (s) => ({
  ...TYCOON,
  jokers: Math.min(4, Math.max(2, Number(s.jokers) || 2))
});
