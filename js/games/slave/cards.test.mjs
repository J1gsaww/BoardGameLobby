/* cards.test.mjs — ชุดทดสอบฟังก์ชันเปรียบเทียบไพ่
   รันด้วย  node js/games/slave/cards.test.mjs
   ไม่ต้องลงอะไรเพิ่ม ไม่ต้องเปิดเบราว์เซอร์ */

import {
  beats, readCombo, compareCombo, forcedLead, bestCards,
  deal, makeDeck, fleeDirection, cardValue, sortHand, OPENING_CARD
} from './cards.js';

let pass = 0, fail = 0;
const ok = (label, got, want = true) => {
  const good = JSON.stringify(got) === JSON.stringify(want);
  if (good) { pass++; }
  else { fail++; console.log(`  ไม่ผ่าน: ${label}\n    ได้    ${JSON.stringify(got)}\n    ควรได้ ${JSON.stringify(want)}`); }
};
const group = (name) => console.log('\n' + name);

/* ── ลำดับไพ่พื้นฐาน ─────────────────────────────── */
group('ลำดับแต้มและดอก');
ok('4 ดอกจิก ใหญ่กว่า 3 โพดำ', beats(['4C'], ['3S']));
ok('3 โพดำ ไม่ใหญ่กว่า 4 ดอกจิก', beats(['3S'], ['4C']), false);
ok('2 โพดำ คือใบใหญ่สุดของสำรับ',
   makeDeck().sort((a, b) => cardValue(a) - cardValue(b)).pop(), '2S');
ok('ดอกจิก 3 คือใบเล็กสุดของสำรับ',
   makeDeck().sort((a, b) => cardValue(a) - cardValue(b))[0], OPENING_CARD);
ok('แต้มเท่ากัน โพดำชนะโพแดง', beats(['7S'], ['7H']));
ok('แต้มเท่ากัน ข้าวหลามตัดชนะดอกจิก', beats(['7D'], ['7C']));
ok('แต้มเท่ากัน ดอกเล็กกว่าแพ้', beats(['7C'], ['7D']), false);
ok('ใบเดียวกันเป๊ะ ทับกันไม่ได้', beats(['9H'], ['9H']), false);

/* ── สายคี่ สายคู่ ───────────────────────────────── */
group('สายคี่กับสายคู่');
ok('เดี่ยวลงทับคู่ไม่ได้', beats(['2S'], ['3C', '3D']), false);
ok('คู่ลงทับเดี่ยวไม่ได้', beats(['2S', '2H'], ['3C']), false);
ok('ตองลงทับคู่ไม่ได้', beats(['5C', '5D', '5H'], ['4C', '4D']), false);
ok('โฟร์ลงทับตองไม่ได้', beats(['5C', '5D', '5H', '5S'], ['4C', '4D', '4H']), false);

/* ── ชุดใหญ่ชนะชุดเล็กในสายเดียวกัน ─────────────── */
group('ชุดใหญ่ชนะชุดเล็ก');
ok('ตองสาม ชนะ โพดำ 2 ใบเดียว', beats(['3C', '3D', '3H'], ['2S']));
ok('โพดำ 2 ใบเดียว ไม่ชนะตองสาม', beats(['2S'], ['3C', '3D', '3H']), false);
ok('โฟร์สาม ชนะ คู่สอง', beats(['3C', '3D', '3H', '3S'], ['2C', '2D']));
ok('คู่สอง ไม่ชนะโฟร์สาม', beats(['2C', '2D'], ['3C', '3D', '3H', '3S']), false);
ok('ตองใหญ่ชนะตองเล็ก', beats(['8C', '8D', '8H'], ['5C', '5D', '5H']));
ok('ตองเล็กไม่ชนะตองใหญ่', beats(['5C', '5D', '5H'], ['8C', '8D', '8H']), false);

/* ── กฎคู่ ดอกสูงสุดในชุดตัดสิน ─────────────────── */
group('คู่แต้มเดียวกัน ตัดสินด้วยดอกสูงสุดในชุด');
ok('คู่ที่มีโพดำ ชนะคู่ที่ไม่มี (โพดำ+โพแดง ปะทะ ข้าวหลาม+ดอกจิก)',
   beats(['7S', '7H'], ['7D', '7C']));
ok('คู่ที่มีโพดำ ชนะ (โพดำ+ข้าวหลาม ปะทะ โพแดง+ดอกจิก)',
   beats(['7S', '7D'], ['7H', '7C']));
ok('คู่สองใบดำ ชนะคู่สองใบแดง', beats(['7S', '7C'], ['7H', '7D']));
ok('คู่สองใบแดง ไม่ชนะคู่ที่มีโพดำ', beats(['7H', '7D'], ['7S', '7C']), false);

/* ── ชุดที่ผิดกติกา ─────────────────────────────── */
group('ชุดที่ลงไม่ได้');
ok('สามใบคนละแต้ม ไม่ใช่ตอง', readCombo(['3C', '4D', '5H']), null);
ok('สองใบคนละแต้ม ไม่ใช่คู่', readCombo(['3C', '4D']), null);
ok('ห้าใบ ลงไม่ได้', readCombo(['3C', '3D', '3H', '3S', '4C']), null);
ok('ไม่มีไพ่เลย ลงไม่ได้', readCombo([]), null);
ok('ไพ่ซ้ำใบ ลงไม่ได้', readCombo(['3C', '3C']), null);
ok('รหัสไพ่มั่ว ลงไม่ได้', readCombo(['XX']), null);
ok('ชุดผิดกติกา beats ต้องเป็นเท็จเสมอ', beats(['3C', '4D'], null), false);

/* ── กองว่าง ────────────────────────────────────── */
group('กองว่าง');
ok('กองว่าง ลงเดี่ยวได้', beats(['3C'], null));
ok('กองว่าง ลงคู่ได้', beats(['9H', '9S'], null));
ok('กองว่าง ลงตองได้', beats(['9H', '9S', '9C'], null));

/* ── บังคับลงเมื่อหมดเวลา ───────────────────────── */
group('บังคับลงไพ่เลขน้อยสุด');
ok('มือ 3C 3D 7H KS ลงคู่สามทั้งชุด',
   forcedLead(['3C', '3D', '7H', 'KS']), ['3C', '3D']);
ok('มีใบเดียวก็ลงใบเดียว',
   forcedLead(['5H', '9C', '2S']), ['5H']);
ok('ถือตองเลขเล็กสุด เสียทั้งตอง',
   forcedLead(['6C', '6D', '6H', 'AS']), ['6C', '6D', '6H']);
ok('รอบแรกจะหยิบดอกจิก 3 ติดมาเสมอ',
   forcedLead(['3C', '3S', 'KD']).includes(OPENING_CARD));

/* ── ไพ่ที่สลาฟต้องยกให้ ────────────────────────── */
group('สลาฟยกไพ่ดีที่สุดให้คิง');
ok('เลือกสองใบสูงสุด เรียงจากสูงลงต่ำ',
   bestCards(['3C', 'AS', '9H', '2D', 'KC'], 2), ['2D', 'AS']);
ok('เลือกใบเดียวได้ไพ่สูงสุด',
   bestCards(['3C', 'AS', '9H'], 1), ['AS']);

/* ── แจกไพ่ ─────────────────────────────────────── */
group('การแจกไพ่');
{
  const h4 = deal(4);
  ok('4 คน ได้คนละ 13 ใบ', h4.map(h => h.length), [13, 13, 13, 13]);
  ok('4 คน รวมกันครบ 52 ใบไม่ซ้ำ', new Set(h4.flat()).size, 52);

  const h10 = deal(10);
  ok('10 คน แจกวนจนหมดสำรับ', h10.map(h => h.length), [6, 6, 5, 5, 5, 5, 5, 5, 5, 5]);
  ok('10 คน รวมกันครบ 52 ใบไม่ซ้ำ', new Set(h10.flat()).size, 52);
  ok('มือที่แจกมาเรียงจากเล็กไปใหญ่แล้ว',
     JSON.stringify(h4[0]), JSON.stringify(sortHand(h4[0])));
}

/* ── ทิศหนีคิง ──────────────────────────────────── */
group('ทิศหนีคิง (1 = ตามเข็ม, -1 = ทวนเข็ม)');
ok('5 ที่นั่ง สลาฟที่ 0 คิงที่ 2 → ทวนเข็มไกลกว่า', fleeDirection(0, 2, 5), -1);
ok('5 ที่นั่ง สลาฟที่ 0 คิงที่ 3 → ตามเข็มไกลกว่า', fleeDirection(0, 3, 5), 1);
ok('6 ที่นั่ง คิงอยู่ตรงข้ามพอดี ระยะเท่ากัน → ทวนเข็ม', fleeDirection(0, 3, 6), -1);
ok('4 ที่นั่ง คิงอยู่ตรงข้ามพอดี → ทวนเข็ม', fleeDirection(0, 2, 4), -1);
ok('ไม่มีคิง (รอบแรก) → ตามเข็ม', fleeDirection(0, null, 5), 1);

/* ── สรุป ───────────────────────────────────────── */
console.log(`\n${'─'.repeat(46)}\nผ่าน ${pass} · ไม่ผ่าน ${fail}\n`);
process.exit(fail ? 1 : 0);
